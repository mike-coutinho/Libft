#include "libft.h"

int ft_memcmp(const void *s1, const void *s2, size_t n)
{
	char *ptr1;
	char *ptr2;
	
	ptr1 = (char *)s1;
	ptr2 = (char *)s2;
	if (n == 0)
		return (0);
	while (n > 0)
	{
		if (*ptr1 != *ptr2)
			return (*ptr1 - *ptr2);
		ptr1++;
		ptr2++;
		n--;
	}
	return (0);
}